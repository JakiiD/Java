public class Relasi {
    public static void main(String args[]) {
        int a = 10;
        int b = 4;
        System.out.println("Operator Logika"); // Note: This should likely be "Operator Relasi"
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
    }
}
