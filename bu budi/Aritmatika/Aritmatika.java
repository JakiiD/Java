public class Aritmatika {
    public static void main(String args[]) {
        int x;
        int y;
        x = 15;
        y = 4;
        System.out.println("Nilai x+y : " + (x + y));
        System.out.println("Nilai x-y : " + (x - y));
        System.out.println("Nilai x/y : " + (x / y));
        System.out.println("Nilai x*y : " + (x * y));
    }
}
