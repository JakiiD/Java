public class switch1 {
    public static void main(String args[]) {
        int nilai = 2;
        switch (nilai) {
            case 1:
                System.out.println("Satu");
                break;
            case 2:
                System.out.println("Dua");
                break;
            case 3:
                System.out.println("Tiga");
                break;
            default:
                System.out.println("Tdk ada dalam pilihan");
        }
    }
}
